package Single;

public class Student {

	String name,collegeChoice;
	int rank,college_alot;
	public int getCollege_alot() {
		return college_alot;
	}
	public void setCollege_alot(int college_alot) {
		this.college_alot = college_alot;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCollege_choice() {
		return collegeChoice;
	}
	public void setCollege_choice(String college_choice) {
		this.collegeChoice = college_choice;
	}
	public int getRank() {
		return rank;
	}
	public void setRank(int rank) {
		this.rank = rank;
	}
	
	
}
